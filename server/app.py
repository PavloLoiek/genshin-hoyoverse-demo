@app.get("/api/player/search", tags=["Account Recovery"])
async def search_player(
    last_topup_amount_usd: Optional[float] = Query(None),
    last_topup_date: Optional[str] = Query(None),
    device_model: Optional[str] = Query(None),
    server_region: Optional[str] = Query(None),
    last_5star_pulled: Optional[str] = Query(None),
    adventure_rank: Optional[int] = Query(None),
    hoyoverse_email: Optional[str] = Query(None),
):
    """
    Knowledge-based player search for account recovery when the caller does not have their UID.
    Requires at least 3 matching fields to return a result (prevents false positives).
    """
    filters = {
        "last_topup_amount_usd": last_topup_amount_usd,
        "last_topup_date": last_topup_date,
        "device_model": device_model,
        "server_region": server_region,
        "last_5star_pulled": last_5star_pulled,
        "adventure_rank": adventure_rank,
        "hoyoverse_email": hoyoverse_email,
    }
    provided = {k: v for k, v in filters.items() if v is not None and v != ""}
    if len(provided) < 3:
        raise HTTPException(
            status_code=400,
            detail=(
                "Knowledge-based search requires at least 3 fields. "
                f"Provided: {len(provided)}. Available fields: {', '.join(filters.keys())}"
            ),
        )

    matches = []
    for uid, player in SEED_DATA.get("players", {}).items():
        score = 0
        for key, value in provided.items():
            pv = player.get(key)
            if pv is None:
                continue
            if isinstance(pv, str) and isinstance(value, str):
                if pv.strip().lower() == value.strip().lower():
                    score += 1
            else:
                if pv == value:
                    score += 1
        if score >= 3:
            matches.append({"uid": uid, "match_score": score, "name": player.get("name")})

    if not matches:
        raise HTTPException(
            status_code=404,
            detail="No player matched the provided criteria with confidence. Try escalating to a human agent.",
        )
    matches.sort(key=lambda m: -m["match_score"])
    return {"status": "success", "count": len(matches), "data": matches}


