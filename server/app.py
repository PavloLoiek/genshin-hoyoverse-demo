@app.post("/api/account/verify_ownership", tags=["Account Recovery"])
async def verify_ownership(body: VerifyOwnershipRequest):
    """
    Verify ownership of a UID by matching caller-provided knowledge-based answers
    against the stored account. Returns a match score 0-5.
    """
    if not body.uid:
        raise HTTPException(status_code=400, detail="uid is required")
    _, player = _case_insensitive_lookup(SEED_DATA.get("players", {}), body.uid)
    if player is None:
        raise HTTPException(status_code=404, detail=f"Player {body.uid} not found")

    checks = []
    score = 0
    fields = [
        ("last_topup_amount_usd", body.last_topup_amount_usd),
        ("last_topup_date", body.last_topup_date),
        ("last_5star_pulled", body.last_5star_pulled),
        ("device_model", body.device_model),
        ("adventure_rank", body.adventure_rank),
        ("server_region", body.server_region),
        ("hoyoverse_email", body.hoyoverse_email),
    ]
    for field, provided in fields:
        if provided is None:
            continue
        stored = player.get(field)
        match = False
        if stored is not None:
            if isinstance(stored, str) and isinstance(provided, str):
                match = stored.strip().lower() == provided.strip().lower()
            else:
                match = stored == provided
        checks.append({"field": field, "match": match})
        if match:
            score += 1

    verified = score >= 3
    return {
        "status": "success",
        "data": {
            "uid": body.uid,
            "verified": verified,
            "match_score": score,
            "checks": checks,
            "threshold": 3,
        },
    }


